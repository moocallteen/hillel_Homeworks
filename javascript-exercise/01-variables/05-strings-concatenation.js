console.log('one word');
a = 'hello';
b = 'world';

console.log(a + b); // one word

// with extra space
console.log('with extra space'); 
console.log(a + ' ' + b); // two words

console.log('space in the variable'); 
c = 'hello ';
d = 'world';

console.log(c + d); // two words

// template string literal
console.log('template string literal'); 
const greeting = `${a} ${b}`;

console.log(greeting); // two words

